// 为没有定义的个性提供默认短语（使用豆包的作为基底）
const DEFAULT_PERSONALITY = '豆包';

/**
 * 获取个性化回退短语
 * @param {string} playerName - 玩家名字
 * @param {string} role - 角色 key（VILLAGER/WOLF/...）
 * @param {string} actionType - 'speak' | 'lastWords'
 * @returns {string} 随机选取的个性化短语
 */
function getFallbackPhrase(playerName, role, actionType = 'speak') {
    const phrases = PERSONALITY_PHRASES[playerName] || PERSONALITY_PHRASES[DEFAULT_PERSONALITY];

    if (actionType === 'last_words' || actionType === 'lastWords') {
        const pool = phrases.lastWords || PERSONALITY_PHRASES[DEFAULT_PERSONALITY].lastWords;
        return pool[Math.floor(Math.random() * pool.length)];
    }

    const rolePool = phrases.speak?.[role] || phrases.speak?.VILLAGER || PERSONALITY_PHRASES[DEFAULT_PERSONALITY].speak.VILLAGER;
    return rolePool[Math.floor(Math.random() * rolePool.length)];
}

/**
 * 获取个性化回退投票决策
 * 基于角色给出有逻辑的投票（而非纯随机）
 */
function getFallbackVote(player, validTargets, gameState) {
    // 简单启发式：优先投最近发言最少/最短的玩家（"划水"嫌疑）
    // 但这比纯随机好
    if (!validTargets || validTargets.length === 0) return null;
    return validTargets[Math.floor(Math.random() * validTargets.length)];
}

module.exports = { getFallbackPhrase, getFallbackVote, PERSONALITY_PHRASES };
